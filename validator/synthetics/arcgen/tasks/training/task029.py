from validator.synthetics.arcgen import common


def generate(width=None, height=None, rows=None, cols=None, idxs=None,
             colors=None, zoom_width=None, zoom_height=None, row=None, col=None,
             zoom_color=None):
  """Returns input and output grids according to the given parameters.

  Args:
    width: the width of the grid
    height: the height of the grid
    rows: a list of vertical coordinates where pixels should be placed
    cols: a list of horizontal coordinates where pixels should be placed
    idxs: a list of digits indexing into the colors[] list
    colors: the colors used to add the random static
    zoom_width: the width of the zoom
    zoom_height: the height of the zoom
    row: the row where the zoom starts
    col: the column where the zoom starts
    zoom_color: the color of the zoom border
  """
  if width is None:
    width, height = common.randint(10, 25), common.randint(10, 25)
    num_colors = common.randint(3, 4)
    rows, cols, idxs = [], [], []
    for r in range(height):
      for c in range(width):
        idx = common.randint(0, num_colors)
        if idx == num_colors: continue
        rows.append(r)
        cols.append(c)
        idxs.append(idx)
    zoom_color = common.random_color()
    colors = common.random_colors(num_colors, exclude=[zoom_color])
    zoom_width = common.randint(1, width - 2)
    zoom_height = common.randint(1, height - 2)
    row = common.randint(1, height - zoom_height - 1)
    col = common.randint(1, width - zoom_width - 1)

  grid = common.grid(width, height)
  output = common.grid(zoom_width, zoom_height)
  for r, c, idx in zip(rows, cols, idxs):
    grid[r][c] = colors[idx]
    if r < row or r >= row + zoom_height: continue
    if c < col or c >= col + zoom_width: continue
    output[r - row][c - col] = colors[idx]
  for r in range(row - 1, row + zoom_height + 1):
    grid[r][col - 1] = grid[r][col + zoom_width] = zoom_color
  for c in range(col - 1, col + zoom_width + 1):
    grid[row - 1][c] = grid[row + zoom_height][c] = zoom_color
  return {"input": grid, "output": output}


